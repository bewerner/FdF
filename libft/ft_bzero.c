#include "libft.h"

void	ft_bzero(void *s, size_t len)
{
	size_t	i;
	char	*s_ptr;

	i = 0;
	s_ptr = s;
	while (i < len)
	{
		s_ptr[i] = 0;
		i++;
	}
}
